#!/usr/bin/env python3
"""
Defines a function that adds two numbers
"""


def add(a: float, b: float) -> float:
    """
    Adds two numbers together and returns the result.

    Parameters:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of the two numbers.
    """
    return a + b
