#!/usr/bin/env python3
"""module concat"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings together and returns the result.

    Parameters:
        str1 (str): The first string to concatenate.
        str2 (str): The second string to concatenate.

    Returns:
        str: The concatenated string.
    """
    return str1 + str2
